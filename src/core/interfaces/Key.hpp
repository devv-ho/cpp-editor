// Defines Key, the editor's internal key vocabulary.

#pragma once

namespace editor::core {

enum class Key {
    H,
    J,
    K,
    L,
    G_lower,  // g
    G_upper,  // G
    Dollar,   // $
    Zero,     // 0
    I,
    A,
    Escape,
    Backspace,
    Return,
};

}  // namespace editor::core
